// Metodos de array: shift e unshift
// - shift: remove o primeiro elemento de um array;
// - unshift: adiciona um ou mais elementos no inicio de um array;
// - sempre no inicio

let verdurasLegumes = ["alface", "couve", "almeirão", "Tomate"]

let verduraLegumesRemovido = verdurasLegumes.shift();

console.log(verduraLegumesRemovido);

console.log(verdurasLegumes);

let verduraLegumesAdicionar = verdurasLegumes.unshift("Cenoura", "Agrião");

console.log(verdurasLegumes);

