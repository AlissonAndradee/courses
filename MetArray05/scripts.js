// Metodos de array: forEach
// - Itera cada elemento do array;

let nomes = ["mateus", "andre", "Jose", "maria", "joao"];

nomes.forEach(nome =>{
    console.log("O nome é " + nome);
});

let frutas = ["maçã", "goiaba", "ameixa", "mamão", "banana"];

frutas.forEach(fruta => {
    console.log("Eu gosto de " + fruta);
});

